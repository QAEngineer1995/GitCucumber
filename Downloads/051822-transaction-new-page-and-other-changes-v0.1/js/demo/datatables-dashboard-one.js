$(document).ready(function() {
    var table = $('#dashboard-table').DataTable({
        /*'ajax': 'https://gyrocode.github.io/files/jquery-datatables/objects.json',*/
        
        'columns': [
            { 'data': 'id', 'width': '50px'},
            { 'data': 'Priority', 'width': '70px'},
            { 'data': 'Insured', 'width': '200px'},
            { 'data': 'Policy-Status', 'width': '120px'},
            { 'data': 'Workflow', 'width': '150px'},
            { 'data': 'Effective-Date', 'width': '150px'},
            { 'data': 'Expiration-Date', 'width': '150px'},
            { 'data': 'Assigned-To', 'width': '200px'},
            { 'data': 'Underwriter', 'width': '100px'},
            { 'data': 'Broker', 'width': '200px'},
            { 'data': 'Created-By', 'width': '200px'},
            { 'data': 'Created-Date', 'width': '120px'},
            { 'data': 'Coverages', 'width': '150px'},
            { 'data': 'Dropdown-menu', 'width': '30px'},
            { 'data': 'accordian-menu', 'width': '25px'}
        ],
        'pagingType': 'simple',
        'responsive': true,
        'scrollX': true,
        'fixedColumns':   {
            leftColumns: 0,
            rightColumns: 2
        },
        'ordering': false,
        'language': {
            paginate: {
              next: '<img src="img/right-black-chevron.svg">', // or '→'
              previous: '<img src="img/left-black-chevron.svg">' // or '←' 
            }
          },
        "dom": '<"float-left"><"float-right">rt<"row"<"col-sm-5"l><"col-sm-3"l><"col-sm-3"i><"col-sm-1"p>>'
    } );
$("#dashboard-table_length").addClass('mt-2').html('<span class="text-gray-500">Total Rows: <b>25</b></span>');
    // Add event listener for opening and closing details
    $('#dashboard-table tbody').on('click', 'td.details-control', function(){
        var tr = $(this).closest('tr');
        var row = table.row( tr );

        if(row.child.isShown()){
            // This row is already open - close it
            row.child.hide();
            tr.removeClass('shown');
        } else {
            // Open this row
            row.child(format(row.data())).show();
            tr.addClass('shown');
        }
    });

    // Handle click on "Expand All" button
    $('#btn-show-all-children').on('click', function(){
        // Enumerate all rows
        table.rows().every(function(){
            // If row has details collapsed
            if(!this.child.isShown()){
                // Open this row
                this.child(format(this.data())).show();
                $(this.node()).addClass('shown');
            }
        });
    });

    // Handle click on "Collapse All" button
    $('#btn-hide-all-children').on('click', function(){
        // Enumerate all rows
        table.rows().every(function(){
            // If row has details expanded
            if(this.child.isShown()){
                // Collapse row details
                this.child.hide();
                $(this.node()).removeClass('shown');
            }
        });
    });

    $('#selectAll').change(function () {
    $('.cb-element').prop('checked',this.checked);
});

$('.cb-element').change(function () {
 if ($('.cb-element:checked').length == $('.cb-element').length){
  $('#selectAll').prop('checked',true);
 }
 else {
  $('#selectAll').prop('checked',false);
 }
});
});
